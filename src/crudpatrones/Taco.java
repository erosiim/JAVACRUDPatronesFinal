package crudpatrones;

/**
 *
 * @author exkapp
 */
public class Taco {
    private int id;
    private String tipo;
    private int precio;
    
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String sabor) {
        this.tipo = sabor;
    }

    public int getPrecio() {
        return precio;
    }

    public void setPrecio(int precio) {
        this.precio = precio;
    }
    
    
    
}
